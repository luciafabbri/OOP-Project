package utility;

public class UpDownLeftRight {
	
	private final int up;
	private final int down;
	private final int left;
	private final int right;
	
	public UpDownLeftRight(int up, int down, int left, int right) {
		this.up = up;
		this.down = down;
		this.left = left;
		this.right = right;
	}

	public int getUp() {
		return up;
	}

	public int getDown() {
		return down;
	}

	public int getLeft() {
		return left;
	}

	public int getRight() {
		return right;
	}
	
	

}
